package org.openscada.da.server.opc2.configuration;

public class ItemDescription
{
    private final String id;

    private final String description;

    private final String accessPath;

    public ItemDescription ( final String id, final String description, final String accessPath )
    {
        this.id = id;
        this.description = description;
        this.accessPath = accessPath;
    }

    public ItemDescription ( final String id )
    {
        this.id = id;
        this.description = null;
        this.accessPath = null;
    }

    public String getId ()
    {
        return this.id;
    }

    public String getDescription ()
    {
        return this.description;
    }

    @Override
    public int hashCode ()
    {
        final int prime = 31;
        int result = 1;
        result = prime * result + ( this.id == null ? 0 : this.id.hashCode () );
        return result;
    }

    @Override
    public boolean equals ( final Object obj )
    {
        if ( this == obj )
        {
            return true;
        }
        if ( obj == null )
        {
            return false;
        }
        if ( getClass () != obj.getClass () )
        {
            return false;
        }
        final ItemDescription other = (ItemDescription)obj;
        if ( this.id == null )
        {
            if ( other.id != null )
            {
                return false;
            }
        }
        else if ( !this.id.equals ( other.id ) )
        {
            return false;
        }
        return true;
    }

    public String getAccessPath ()
    {
        return this.accessPath;
    }
}
