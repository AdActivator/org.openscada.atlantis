package org.eclipse.scada.ae.server.http.filter;

import org.eclipse.scada.ae.Event;

public abstract class FilterEntry
{
    private final String id;

    private final long priority;

    public FilterEntry ( final String id, final long priority )
    {
        this.id = id;
        this.priority = priority;
    }

    public String getId ()
    {
        return this.id;
    }

    public long getPriority ()
    {
        return this.priority;
    }

    public abstract boolean matches ( Event event );

    @Override
    public int hashCode ()
    {
        final int prime = 31;
        int result = 1;
        result = prime * result + ( this.id == null ? 0 : this.id.hashCode () );
        return result;
    }

    @Override
    public boolean equals ( final Object obj )
    {
        if ( this == obj )
        {
            return true;
        }
        if ( obj == null )
        {
            return false;
        }
        if ( getClass () != obj.getClass () )
        {
            return false;
        }
        final FilterEntry other = (FilterEntry)obj;
        if ( this.id == null )
        {
            if ( other.id != null )
            {
                return false;
            }
        }
        else if ( !this.id.equals ( other.id ) )
        {
            return false;
        }
        return true;
    }

}
